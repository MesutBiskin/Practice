package Emir_Practice;

public class swapTwoNumbers {
    public static void main(String[] args) {
        int k=10;
        int m=15;

        System.out.println("degisim Oncesi");
        System.out.println("k="+k);
        System.out.println("m="+m);
        int gec = k;
        k=m;
        m=gec;
        System.out.println("degisim sonrasi");
        System.out.println("k="+k);
        System.out.println("m="+m);

    }
}
